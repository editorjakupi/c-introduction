// Typdefinitioner för olika storlekar av heltal
typedef char int8;           // 8-bitars signed heltal
typedef short int16;         // 16-bitars signed heltal
typedef int int32;           // 32-bitars signed heltal
typedef long long int int64; // 64-bitars signed heltal

// Alias för signed typer
typedef char s8;
typedef short s16;
typedef int s32;
typedef long long int s64;

// Alias för unsigned typer
typedef unsigned char u8; // Fixad definition